    // 入力ボックスに文字の挿入
    static add_chara(str, selIdx, inputId)
    {
      // テキストボックスの値を取得
      var text_sentence = $(inputId).val();
      // 入力欄に入れる
      $(inputId).val(Input_Botton.strIns(text_sentence, selIdx, str));
    }

    // 文字列の挿入
    static strIns(str, idx, val){
        // カーソルの位置に文字を入れる
        var res = str.slice(0, idx) + val + str.slice(idx);
        // 結果を返す
        return res;
    };

